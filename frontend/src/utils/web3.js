// 让用户连接钱包
async function connectWallet() {
  if (window.ethereum) {
    try {
      // 请求钱包连接
      await window.ethereum.request({ method: 'eth_requestAccounts' });
      console.log("钱包连接成功!");
    } catch (error) {
      console.error("连接钱包失败:", error);
    }
  } else {
    console.log("请安装 MetaMask 钱包!");
  }
}


